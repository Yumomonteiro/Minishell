#include "libft.h"

void	ft_lstadd_back(t_list **lst, int data)
{
	t_list	*new;
	t_list	*last;

	new = ft_lstnew(data);
	if (!data)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last = *lst;
	while (last->next != NULL)
		last = last->next;
	last->next = new;
}
